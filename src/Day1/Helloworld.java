package Day1;

/*
 * public : 접근제한자, 누구나 다 접근 가능
 * 접근제한자는 외부에서 해당하는 클래스 또는 메소드에 접근을 허용
 * 할지 말지를 결정
 * class : 클래스임을 나타냄
 * */
public class Helloworld {

	/*
	 * static : 클래스를 통해 호출함
	 * void : 반환이 없음, 메소드 설명에서 자세히 할 예정
	 * main : 메소드 이름
	 * (string[] args) : 매개변수
	 * 자바에서 프로그램을 실행하려면 main 메소드가 있어야한다.!!
	* */
	public static void main(String[] args) {
		// 표준 출력 메소드
		System.out.println("hello world!");
		}
}
